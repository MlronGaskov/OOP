package ru.nsu.gaskov.core;

/**
 * A class that contains constants used in the game of Blackjack.
 */
public class Constants {

    /**
     * The score at which the dealer must stand.
     * Typically, this is 17 in standard Blackjack rules.
     */
    public static final int DEALER_STAND_SCORE = 17;

    /**
     * The score needed to achieve a Blackjack.
     * This is the highest possible score, equal to 21.
     */
    public static final int BLACKJACK_SCORE = 21;
}
